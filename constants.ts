/**
 * 将各赛道 prompt 中「固定 10 条选题」的表述替换为用户选择数量，并追加数量铁律（策划选题专用）。
 */
export function applyTopicCountToPrompt(prompt: string, count: number): string {
  const n = Math.min(50, Math.max(1, Math.floor(Number(count)) || 5));
  let p = prompt;
  p = p.replace(/输出10个策划选题标题/g, `输出${n}个策划选题标题`);
  p = p.replace(/输出10个策划选题/g, `输出${n}个策划选题`);
  p = p.replace(/输出10个爆款选题/g, `输出${n}个爆款选题`);
  p = p.replace(/输出10个极具爆款潜质的选题/g, `输出${n}个极具爆款潜质的选题`);
  p = p.replace(/输出10个极具爆款潜力的标题/g, `输出${n}个极具爆款潜力的标题`);
  p = p.replace(/输出10个标题/g, `输出${n}个标题`);
  p = p.replace(
    /只输出 10 个标题，每行一个，不要编号，不要前言，不要解释。标题不要出现【短视频】【长视频】或类似标签。/g,
    `只输出 ${n} 个标题，每行一个，不要编号，不要前言，不要解释。标题不要出现【短视频】【长视频】或类似标签。`
  );
  p = p.replace(
    /只输出 10 个标题，每行一个，不要编号，不要前言，不要解释。/g,
    `只输出 ${n} 个标题，每行一个，不要编号，不要前言，不要解释。`
  );
  p = p.replace(/只输出 10 个标题/g, `只输出 ${n} 个标题`);
  p = p.replace(/只输出10个标题/g, `只输出${n}个标题`);
  p = p.replace(/生成 \*\*10 个\*\*/g, `生成 **${n} 个**`);
  p = p.replace(/列举 \*\*10 个\*\*/g, `列举 **${n} 个**`);
  p = p.replace(/只输出 \*\*10 个\*\*/g, `只输出 **${n} 个**`);
  p = p.replace(/生成 \*\*10 個\*\*/g, `生成 **${n} 個**`);
  p = p.replace(/直接輸出 10 個標題/g, `直接輸出 ${n} 個標題`);
  p = p.replace(/其后输出 10 个标题/g, `其后输出 ${n} 个标题`);
  p = p.replace(
    /只输出 10 个标题，每行一个，无编号、无前言、无分析。/g,
    `只输出 ${n} 个标题，每行一个，无编号、无前言、无分析。`
  );
  p += `\n\n【数量铁律·最高优先级】本次必须恰好输出 ${n} 个标题（或选题），每行一个，共 ${n} 行；不要多也不要少；若上文出现其他数字，一律以本段 ${n} 为准。`;
  return p;
}