  - 家运乱=屋内阴影笼罩，灯火昏暗摇曳
  - 对比构图=左右分屏：左侧温润明亮（白黄）vs右侧灰暗沉重（黑蓝）
  - 关键转折点=大师侧影立于诊室药柜前，目光锐利注视前方
- 金融投资赛道：芒格式智者坐在书房，手握书本，窗外K线若隐若现
- 心理学赛道：知性女性立于窗前，霓虹光影投射在脸上
